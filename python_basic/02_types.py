###
# Python tiene varios tipos de datos
# int, float, str, bool, NoneType, list, touple, dict, range, set...
###

## int = numeros enteros positivos y negativos
print(type("int = 1,2,3,4,-5,-6..."))

## float = numeros decimales
print(type("float = 3.14, 3.12, 2.2..."))

## complex = tienen una parte real y una imaginaria
print(type(1 + 2j))

## booleano = verdadero y falso
print(type(1 > 2))
print(type(1 < 2))

## None = no tiene nada ningun valor asignado
print(type(None))