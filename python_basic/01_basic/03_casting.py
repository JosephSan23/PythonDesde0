###
# Transformar un tipo de valor a otro
###

print("Conversion de tipos")
print(int("100") + 2)
print("100" + str(2))

## En los booleanos el 0 es el unico numero falso los demas daran positivo
print(bool(3))
print(bool(0))
print(bool(-1))

## Los datos deben ser validos para poder transformarlos
## Con los numeros decimales redondeara al mas cercano si es 1.50 sera 1, si es 1.51 sera 2.
